#include "ft_printf.h"

int	ft_putnbr(long nbr)
{
	int	z;

	z = 0;
	if (nbr < 0)
	{
		z += ft_putchar('-');
		nbr *= -1;
	}
	if (nbr > 9)
		z += ft_putnbr(nbr / 10);
	z += ft_putchar((nbr % 10) + 48);
	return (z);
}
